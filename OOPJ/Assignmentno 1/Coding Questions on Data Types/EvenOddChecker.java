class EvenOddChecker{
     public static void main(String args[]){
	 
	 
	 String s1 = args[0];
	
	 
	 int num = Integer.parseInt(s1);
	 
	 
	String result = (num % 2 == 0)? "Even" : "Odd";
	
	System.out.println(num + " is " + result + ".");
	
	 
	 }
	 
}
















